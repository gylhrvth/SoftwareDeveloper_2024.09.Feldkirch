package Erik.herbstferien_aufgaben.selbsttest;

// Zähle in einer for-Schleife rückwärts von 10 bis inklusive 0 und gib jede Zahl aus.

public class SchleifenAufgabe_1 {
    public static void main(String[] args) {
        for (int i = 10; i > -1; i--) {
            System.out.print(i + " ");
        }
    }
}